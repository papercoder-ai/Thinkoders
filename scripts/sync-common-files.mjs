import fs from "node:fs"
import path from "node:path"

const root = process.cwd()

const copies = [
  {
    from: path.join(root, "leave-domain-common.ts"),
    to: path.join(root, "Attendace_system-main", "lib", "leave-domain-common.ts"),
  },
  {
    from: path.join(root, "leave-module-page.tsx"),
    to: path.join(root, "Attendace_system-main", "lib", "leave-module-shared.ts"),
  },
]

for (const item of copies) {
  if (!fs.existsSync(item.from)) {
    throw new Error(`Missing root source file: ${item.from}`)
  }

  fs.mkdirSync(path.dirname(item.to), { recursive: true })
  const content = fs.readFileSync(item.from, "utf8")
  const banner =
    "// AUTO-GENERATED FROM WORKSPACE ROOT. DO NOT EDIT THIS FILE DIRECTLY.\n" +
    "// Run from root: npm run sync:common\n\n"
  fs.writeFileSync(item.to, `${banner}${content}`, "utf8")
  console.log(`Synced: ${path.relative(root, item.to)} <- ${path.relative(root, item.from)}`)
}

console.log("Common files synced from root to Attendance runtime mirrors.")
